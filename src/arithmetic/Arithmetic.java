package arithmetic;

public class Arithmetic{
	public static void main(String[] args) {
		int i = 10;
		int j = 5;
		int a = i+j;
		int b = i - j;
		int c = i*j;
		int d = i /j;
		
		System.out.println(i);
		System.out.println(j);
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
	}
}
